                    <h2 class="text-primary">¿Qué es la Visa U?</h2>
                    <p>El estatus No Inmigrante U (Visa U) se destina a aquellas personas que han sido víctimas de determinados crímenes y han sufrido abuso físico o psicológico. Este estatus permite a las víctimas colaborar con las autoridades policiales y los funcionarios gubernamentales en la investigación o persecución de actividades delictivas.</p>
